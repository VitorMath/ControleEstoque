    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String senha = null;

        if ((fieldSenha.getPassword().equals(null) || fieldUsuario.getText().equals(""))) {

            JOptionPane.showMessageDialog(null, "Usu치rio ou Senha incorretos.");

        } else {

            Conexao dbCon = new Conexao();

            try {
                senha = dbCon.getSenha(fieldUsuario.getText());
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

            char[] senhaFinal = fieldSenha.getPassword();
            String testSenha = "[" + String.valueOf(senhaFinal) + "]";

            if (testSenha.equals(senha)) {
                fieldUsuario.setText("");
                fieldSenha.setText("");
                senha = "";
                testSenha = "";
                senhaFinal = null;

                new Janela().setVisible(true);
                this.dispose();

            } else {
                fieldUsuario.setText("");
                fieldSenha.setText("");
                senha = "";
                testSenha = "";
                senhaFinal = null;

                JOptionPane.showMessageDialog(null, "Usu치rio ou Senha incorretos.");
            }

        }
    }//GEN-LAST:event_btnEntrarActionPerformed

